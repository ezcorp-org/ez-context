// ---------------------------------------------------------------------------
// emit() with formats array tests
// ---------------------------------------------------------------------------

describe("emit() with formats", () => {
  const tempDirs: string[] = [];

  afterEach(async () => {
    for (const dir of tempDirs) {
      if (existsSync(dir)) {
        await rm(dir, { recursive: true, force: true });
      }
    }
    tempDirs.length = 0;
  });

  async function makeTmpDir(): Promise<string> {
    const dir = await mkdtemp(path.join(os.tmpdir(), "ez-context-fmt-test-"));
    tempDirs.push(dir);
    return dir;
  }

  it("dryRun with formats cursor+ruler returns both in rendered map", async () => {
    const reg = makeRegistry();
    const result = await emit(reg, {
      outputDir: "/nonexistent",
      dryRun: true,
      formats: ["cursor", "ruler"],
    });

    expect(result.rendered).toHaveProperty("cursor");
    expect(result.rendered).toHaveProperty("ruler");
    expect(result.filesWritten).toEqual([]);
  });

  it("dryRun cursor render starts with YAML frontmatter", async () => {
    const reg = makeRegistry();
    const result = await emit(reg, {
      outputDir: "/nonexistent",
      dryRun: true,
      formats: ["cursor"],
    });

    expect(result.rendered["cursor"]).toMatch(/^---\n/);
    expect(result.rendered["cursor"]).toContain("alwaysApply: true");
  });

  it("backward compat: no formats defaults to claude+agents", async () => {
    const reg = makeRegistry();
    const result = await emit(reg, {
      outputDir: "/nonexistent",
      dryRun: true,
    });

    expect(result.claudeMd).toContain("# Project Context");
    expect(result.agentsMd).toContain("# AGENTS.md");
    expect(result.rendered).toHaveProperty("claude");
    expect(result.rendered).toHaveProperty("agents");
    expect(result.filesWritten).toEqual([]);
  });

  it("backward compat claudeMd/agentsMd fields populated when those formats requested", async () => {
    const reg = makeRegistry();
    const result = await emit(reg, {
      outputDir: "/nonexistent",
      dryRun: true,
      formats: ["claude", "agents"],
    });

    expect(result.claudeMd).toBe(result.rendered["claude"]);
    expect(result.agentsMd).toBe(result.rendered["agents"]);
  });

  it("claudeMd/agentsMd are empty string when formats excludes them", async () => {
    const reg = makeRegistry();
    const result = await emit(reg, {
      outputDir: "/nonexistent",
      dryRun: true,
      formats: ["cursor"],
    });

    expect(result.claudeMd).toBe("");
    expect(result.agentsMd).toBe("");
  });

  it("writes .cursor/rules/ directory when format is cursor", async () => {
    const reg = makeRegistry();
    const tmpDir = await makeTmpDir();

    const result = await emit(reg, {
      outputDir: tmpDir,
      dryRun: false,
      formats: ["cursor"],
    });

    const cursorRulesDir = path.join(tmpDir, ".cursor", "rules");
    expect(existsSync(cursorRulesDir)).toBe(true);
    expect(result.filesWritten).toHaveLength(1);
    expect(result.filesWritten[0]).toMatch(/ez-context\.mdc$/);
  });

  it("creates nested .skills/ez-context/ directory for skills format", async () => {
    const reg = makeRegistry();
    const tmpDir = await makeTmpDir();

    const result = await emit(reg, {
      outputDir: tmpDir,
      dryRun: false,
      formats: ["skills"],
    });

    const skillsDir = path.join(tmpDir, ".skills", "ez-context");
    expect(existsSync(skillsDir)).toBe(true);
    expect(result.filesWritten[0]).toMatch(/SKILL\.md$/);
  });

  it("writes correct number of files for multi-format emit", async () => {
    const reg = makeRegistry();
    const tmpDir = await makeTmpDir();

    const result = await emit(reg, {
      outputDir: tmpDir,
      dryRun: false,
      formats: ["claude", "cursor", "ruler"],
    });

    expect(result.filesWritten).toHaveLength(3);
  });

  it("copilot format creates .github/ parent directory when it does not exist", async () => {
    const reg = makeRegistry();
    const tmpDir = await makeTmpDir();

    // Sanity-check: .github does not exist yet
    expect(existsSync(path.join(tmpDir, ".github"))).toBe(false);

    const result = await emit(reg, {
      outputDir: tmpDir,
      dryRun: false,
      formats: ["copilot"],
    });

    expect(existsSync(path.join(tmpDir, ".github"))).toBe(true);
    expect(result.filesWritten).toHaveLength(1);
    expect(result.filesWritten[0]).toMatch(/copilot-instructions\.md$/);
  });
});
