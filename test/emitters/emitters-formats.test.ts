// ---------------------------------------------------------------------------

function makeRegistry(): ConventionRegistry {
  let reg = createRegistry("/test/project");
  reg = {
    ...reg,
    stack: {
      language: "TypeScript",
      framework: "Express",
      testRunner: "Vitest",
      buildTool: "tsdown",
      packageManager: "bun",
    },
    architecture: {
      pattern: "layered",
      layers: ["core", "api", "db"],
    },
  };

  reg = addConvention(reg, {
    category: "naming",
    pattern: "camelCase for variables and functions",
    confidence: 0.9,
    evidence: [],
  });
  reg = addConvention(reg, {
    category: "testing",
    pattern: "test files in test/ directory",
    confidence: 0.85,
    evidence: [],
  });
  reg = addConvention(reg, {
    category: "stack",
    pattern: "build script",
    confidence: 0.95,
    evidence: [],
    metadata: { scriptName: "build", command: "tsdown" },
  });

  return reg;
}

// ---------------------------------------------------------------------------
// renderCursorMdc tests
// ---------------------------------------------------------------------------

describe("renderCursorMdc", () => {
  it("output starts with YAML frontmatter delimiter", () => {
    const reg = makeRegistry();
    const output = renderCursorMdc(reg, 0.7);
    expect(output).toMatch(/^---\n/);
  });

  it("frontmatter contains alwaysApply: true", () => {
    const reg = makeRegistry();
    const output = renderCursorMdc(reg, 0.7);
    expect(output).toContain("alwaysApply: true");
  });

  it("frontmatter contains globs as empty string (not null or omitted)", () => {
    const reg = makeRegistry();
    const output = renderCursorMdc(reg, 0.7);
    // js-yaml renders empty string as 'globs: ""\n' or "globs: ''"
    expect(output).toMatch(/globs: ['"]{0,1}['"]{0,1}/);
    // Crucially, globs field IS present
    expect(output).toContain("globs:");
  });

  it("frontmatter contains description field", () => {
    const reg = makeRegistry();
    const output = renderCursorMdc(reg, 0.7);
    expect(output).toContain("description:");
  });

  it("frontmatter is closed before markdown body", () => {
    const reg = makeRegistry();
    const output = renderCursorMdc(reg, 0.7);
    // Should have opening ---, content, closing ---
    const parts = output.split("---\n");
    expect(parts.length).toBeGreaterThanOrEqual(3);
  });

  it("markdown body contains stack info", () => {
    const reg = makeRegistry();
    const output = renderCursorMdc(reg, 0.7);
    expect(output).toContain("TypeScript");
    expect(output).toContain("## Stack");
  });

  it("markdown body contains conventions", () => {
    const reg = makeRegistry();
    const output = renderCursorMdc(reg, 0.7);
    expect(output).toContain("camelCase for variables and functions");
  });
});

